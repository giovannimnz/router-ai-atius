def load_api_body(raw: str | None) -> bytes | None:
    if raw is None:
        return None
    if raw.startswith("@"):
        return Path(raw[1:]).expanduser().read_bytes()
    return raw.encode("utf-8")


def request_api(
    *,
    method: str,
    path: str,
    base_url: str,
    data: str | None,
    bearer: str | None,
    headers_extra: list[str],
    timeout: float,
    execute: bool,
    require_execute: bool = False,
) -> None:
    base = base_url.rstrip("/")
    path = path if path.startswith("/") else "/" + path
    url = base + path
    headers = {"Accept": "application/json"}
    if data is not None:
        headers["Content-Type"] = "application/json"
    bearer = bearer or os.environ.get("ATIUS_ROUTER_ADMIN_TOKEN")
    if bearer:
        headers["Authorization"] = f"Bearer {bearer}"
    for item in headers_extra or []:
        if ":" not in item:
            raise CliError(f"Header invalido, use Nome: valor: {item}")
        key, value = item.split(":", 1)
        headers[key.strip()] = value.strip()
    method = method.upper()
    if (method in MUTATING_METHODS or require_execute) and not execute:
        print(f"DRY-RUN API: {method} {url}")
        if data:
            print(redact_text(data) if not data.startswith("@") else f"body={data} (conteudo nao exibido)")
        print("Nada foi enviado. Adicione --execute para aplicar.")
        return
    req = urllib.request.Request(url, data=load_api_body(data), method=method, headers=headers)
    try:
        with urllib.request.urlopen(req, timeout=timeout) as resp:
            code = resp.status
            body = resp.read().decode("utf-8", errors="replace")
    except urllib.error.HTTPError as exc:
        code = exc.code
        body = exc.read().decode("utf-8", errors="replace")
    try:
        payload = json.loads(body)
        print(json.dumps(redact_json(payload), ensure_ascii=False, indent=2, sort_keys=True))
    except json.JSONDecodeError:
        print(redact_text(body))
    print(f"\nHTTP {code}", file=sys.stderr)


