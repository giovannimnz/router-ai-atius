def ensure_read_only(sql: str) -> None:
    cleaned = re.sub(r"/\*.*?\*/", " ", sql, flags=re.S)
    cleaned = re.sub(r"--.*?$", " ", cleaned, flags=re.M).strip().lower()
    if not cleaned.startswith(("select", "with", "show", "explain")):
        raise CliError("query aceita apenas SQL read-only: select/with/show/explain.")
    blocked = re.search(
        r"\b(insert|update|delete|drop|alter|truncate|create|grant|revoke|copy|call|do|vacuum|reindex)\b",
        cleaned,
    )
    if blocked:
        raise CliError(f"SQL bloqueado em modo read-only: {blocked.group(1)}")


