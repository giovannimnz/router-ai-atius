def run_sql_actions(args: argparse.Namespace, actions: list[tuple[Resource, str, str]]) -> None:
    resources: list[Resource] = []
    for resource, _sql, _label in actions:
        if resource not in resources:
            resources.append(resource)

    if not getattr(args, "execute", False):
        for _resource, sql, label in actions:
            print(f"# {label}")
            print(build_redacted_write_preview(sql))
            print()
        print("DRY-RUN: nada foi alterado. Adicione --execute para aplicar.")
        return

    backups = [backup_table(resource) for resource in resources]
    for path in backups:
        print(f"Backup antes da escrita: {path}")

    for _resource, sql, label in actions:
        run_psql(sql)
        print(f"Applied: {label}")


