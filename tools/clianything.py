def http_json(url: str, timeout: float = 5.0) -> tuple[int | str, Any]:
    try:
        with urllib.request.urlopen(url, timeout=timeout) as resp:
            body = resp.read().decode("utf-8", errors="replace")
            code: int | str = resp.status
    except urllib.error.HTTPError as exc:
        body = exc.read().decode("utf-8", errors="replace")
        code = exc.code
    except Exception as exc:  # noqa: BLE001 - CLI diagnostic output should include the failure.
        return "error", str(exc)
    try:
        return code, json.loads(body)
    except json.JSONDecodeError:
        return code, body[:400]


def cmd_status(args: argparse.Namespace) -> None:
    require_podman()
    rows: list[dict[str, Any]] = []
    pod = run(["podman", "pod", "inspect", POD_NAME, "--format", "{{.State}}\t{{.NumContainers}}"], check=False)
    rows.append(
        {
            "check": "pod",
            "target": POD_NAME,
            "status": "ok" if pod.returncode == 0 else "fail",
            "detail": pod.stdout.strip() or pod.stderr.strip(),
        }
    )
    ps = run(
        ["podman", "ps", "--filter", f"pod={POD_NAME}", "--format", "{{.Names}}\t{{.Status}}\t{{.Ports}}"],
        check=False,
    )
    rows.append(
        {
            "check": "containers",
            "target": POD_NAME,
            "status": "ok" if ps.returncode == 0 and ps.stdout.strip() else "fail",
            "detail": ps.stdout.strip().replace("\n", " | ")[:500] or ps.stderr.strip(),
        }
    )
    for label, url in [
        ("backend", f"{DEFAULT_BASE_URL}/api/status"),
        ("v1-models", f"{DEFAULT_BASE_URL}/v1/models"),
    ]:
        code, payload = http_json(url)
        status = "fail"
        if label == "v1-models" and code == 401:
            status = "ok"
        elif code == 200:
            status = "ok"
        rows.append({"check": "http", "target": label, "status": status, "detail": f"{code} {format_scalar(payload)}"})
    count_sql = """
        select 'channels' as item, count(*) as count from channels
        union all select 'models', count(*) from models
        union all select 'tokens', count(*) from tokens
        union all select 'users', count(*) from users
        union all select 'logs', count(*) from logs
    """
    try:
        db_rows = run_psql_json(count_sql)
        rows.append({"check": "db", "target": DB_NAME, "status": "ok", "detail": format_scalar(db_rows)})
    except Exception as exc:  # noqa: BLE001
        rows.append({"check": "db", "target": DB_NAME, "status": "fail", "detail": str(exc)})
    print_table(rows)
    if getattr(args, "strict", False):
        bad = [row for row in rows if row["status"] != "ok"]
        if bad:
            summary = ", ".join(f"{row['target']}={row['status']}" for row in bad)
            raise StrictStatusError(f"status --strict encontrou componentes nao-ok: {summary}")


