def redact_json(value: Any, key: str = "") -> Any:
    if isinstance(value, dict):
        return {k: redact_json(v, k) for k, v in value.items()}
    if isinstance(value, list):
        return [redact_json(item, key) for item in value]
    return redact_value(key, value)


def redact_text(value: str) -> str:
    patterns = [
        (r"Bearer\s+[A-Za-z0-9._~+/=-]+", "Bearer <redacted>"),
        (r"sk-[A-Za-z0-9]{10,}", "sk-redacted"),
        (r"(?i)(password|passwd|secret|token|api[_-]?key|authorization)(['\"\s:=]+)([^,'\"\s}]+)", r"\1\2<redacted>"),
    ]
    text = value
    for pattern, repl in patterns:
        text = re.sub(pattern, repl, text)
    return text


