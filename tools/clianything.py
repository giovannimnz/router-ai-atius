def endpoint_requires_execute(entry: dict[str, Any]) -> bool:
    classification = str(entry.get("classification", ""))
    method = str(entry.get("method", "")).upper()
    return method in MUTATING_METHODS or classification in {"api-action", "auth-flow", "external-webhook"}


def endpoint_template_matches(template: str, request_path: str) -> bool:
    if template == request_path:
        return True
    pattern = re.escape(template)
    pattern = re.sub(r"\\\{[^/{}]+\\\}", r"[^/]+", pattern)
    return re.fullmatch(pattern, request_path) is not None


def find_manifest_entry_for_request(method: str, path: str) -> dict[str, Any] | None:
    method = method.upper()
    normalized = path if path.startswith("/") else "/" + path
    request_path = urllib.parse.urlsplit(normalized).path or "/"
    entries = load_endpoint_manifest()
    exact = manifest_by_key(entries).get(endpoint_key(method, request_path))
    if exact:
        return exact
    for entry in entries:
        if str(entry.get("method", "")).upper() != method:
            continue
        if endpoint_template_matches(str(entry.get("path", "")), request_path):
            return entry
    return None


def get_manifest_entry(method: str, path: str) -> dict[str, Any]:
    key = endpoint_key(method, path)
    entry = manifest_by_key(load_endpoint_manifest()).get(key)
    if not entry:
        raise CliError(f"Endpoint nao mapeado no manifesto: {key}. Use coverage --strict para diagnosticar.")
    return entry


def build_endpoint_path(template: str, params: list[str] | None = None, query: list[str] | None = None) -> str:
    path = template
    for key, value in parse_key_values(params or []).items():
        path = path.replace("{" + key + "}", urllib.parse.quote(str(value), safe=""))
    missing = re.findall(r"\{([^}]+)\}", path)
    if missing:
        raise CliError(f"Parametros obrigatorios ausentes para path: {', '.join(sorted(set(missing)))}")
    query_values = parse_key_values(query or [])
    if query_values:
        path += ("&" if "?" in path else "?") + urllib.parse.urlencode(query_values)
    return path


def invoke_manifest_endpoint(
    args: argparse.Namespace,
    method: str,
    path_template: str,
    *,
    params: list[str] | None = None,
    query: list[str] | None = None,
    data: str | None = None,
) -> None:
    entry = get_manifest_entry(method, path_template)
    if entry["classification"] == "unsupported-safe":
        raise CliError(f"Endpoint classificado como unsupported-safe: {method.upper()} {path_template}")
    path = build_endpoint_path(path_template, params=params, query=query)
    request_api(
        method=method,
        path=path,
        base_url=args.base_url,
        data=data,
        bearer=args.bearer,
        headers_extra=args.header,
        timeout=args.timeout,
        execute=getattr(args, "execute", False),
        require_execute=endpoint_requires_execute(entry),
    )


